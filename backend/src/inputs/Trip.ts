import { Field, InputType } from "type-graphql";

@InputType()
export class TripInput {
  @Field()
  title: string;

  @Field()
  date: string;

  @Field()
  price: number;

  @Field()
  passengers: number;

  @Field()
  driverId: string;

  @Field()
  status: string;

  @Field()
  startLocation: string;

  @Field()
  endLocation: string;

  @Field()
  numberOfSeats: number;
}
